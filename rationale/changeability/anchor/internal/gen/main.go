// Command gen emits the two fan-out arms of the public-interface anchor as
// committed generated packages (not runtime-synthesised) so CI re-runs the metric
// on inspectable code — see ../../SCORING.md.
//
//   - decoupled/consumerNN  — depend only on orders.Client; forced-edits = 0 under C1.
//   - coupled/fanout/consumerNN — reach through to backend A's raw row; forced-edits
//     = N under C1 (the migration removes what they name).
//
// The 3 hand-authored patterns under coupled/ (reachthrough/exportrow/locator) are
// the DIVERSITY evidence (distinct realistic couplings); this generated fan-out is
// the N-SCALING arm the contrast measures. Run via
// `go generate ./rationale/changeability/anchor` (cwd = the anchor dir).
package main

import (
	"flag"
	"fmt"
	"os"
	"path/filepath"
)

const decoupledTmpl = `// Code generated by internal/gen. DO NOT EDIT.

// Package consumer%02d depends ONLY on orders.Client (the public boundary), so
// the backend A->B migration forces 0 edits on it. One of N identical dependents;
// the decoupled arm's forced-edit count stays 0 for any N.
package consumer%02d

import (
	"context"

	"github.com/verocorp/tesser-build/rationale/changeability/anchor/orders"
)

// Use exercises the public contract and nothing else.
func Use(ctx context.Context, c orders.Client) error {
	_, err := c.PlaceOrder(ctx, orders.PlaceOrderRequest{CustomerID: "c%02d", SKUs: []string{"x"}})
	return err
}
`

const coupledTmpl = `// Code generated by internal/gen. DO NOT EDIT.

// Package consumer%02d reaches through to backend A's raw row — the coupling the
// backend A->B migration forces to change. One of N identical dependents; the
// coupled arm's forced-edit count is N. Fails to build under -tags swap because
// its own source names backend.OrderRowA / backend.FetchRawA.
package consumer%02d

import (
	"context"

	"github.com/verocorp/tesser-build/rationale/changeability/anchor/backend"
	"github.com/verocorp/tesser-build/rationale/changeability/anchor/orders"
)

// Use places an order, then reaches through to backend A's storage row.
func Use(ctx context.Context, c orders.Client) (backend.OrderRowA, error) {
	resp, err := c.PlaceOrder(ctx, orders.PlaceOrderRequest{CustomerID: "c%02d", SKUs: []string{"x"}})
	if err != nil {
		return backend.OrderRowA{}, err
	}
	return backend.FetchRawA(resp.OrderID), nil
}
`

func emit(dir, tmpl string, n int) {
	for i := 0; i < n; i++ {
		pkgDir := filepath.Join(dir, fmt.Sprintf("consumer%02d", i))
		if err := os.MkdirAll(pkgDir, 0o755); err != nil {
			panic(err)
		}
		content := fmt.Sprintf(tmpl, i, i, i)
		if err := os.WriteFile(filepath.Join(pkgDir, "consumer.go"), []byte(content), 0o644); err != nil {
			panic(err)
		}
	}
}

func main() {
	n := flag.Int("n", 16, "number of consumer packages to emit per arm")
	flag.Parse()

	emit("decoupled", decoupledTmpl, *n)
	emit(filepath.Join("coupled", "fanout"), coupledTmpl, *n)
	fmt.Printf("gen: wrote %d decoupled + %d coupled-fanout consumer packages\n", *n, *n)
}
