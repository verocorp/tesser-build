// Command gen emits the DECOUPLED fan-out: N consumer packages that depend only
// on orders.Client. Committed generated output (not runtime-synthesised) so CI
// re-runs the metric on inspectable code — see ../../SCORING.md.
//
// Run via `go generate ./rationale/changeability/anchor` (cwd = the anchor dir).
// The coupled + red-team arms are authored separately (the adversary step); this
// generator only produces the decoupled arm.
package main

import (
	"flag"
	"fmt"
	"os"
	"path/filepath"
)

const consumerTmpl = `// Code generated by internal/gen. DO NOT EDIT.

// Package consumer%02d is one dependent of the ordering context. It depends
// ONLY on orders.Client (the public boundary), so the backend A->B migration
// forces 0 edits on it. One of N identical dependents; the decoupled arm's
// forced-edit count stays 0 for any N.
package consumer%02d

import (
	"context"

	"github.com/verocorp/go-ddd/rationale/changeability/anchor/orders"
)

// Use exercises the public contract and nothing else.
func Use(ctx context.Context, c orders.Client) error {
	_, err := c.PlaceOrder(ctx, orders.PlaceOrderRequest{CustomerID: "c%02d", SKUs: []string{"x"}})
	return err
}
`

func main() {
	n := flag.Int("n", 16, "number of decoupled consumer packages to emit")
	flag.Parse()

	for i := 0; i < *n; i++ {
		dir := filepath.Join("decoupled", fmt.Sprintf("consumer%02d", i))
		if err := os.MkdirAll(dir, 0o755); err != nil {
			panic(err)
		}
		content := fmt.Sprintf(consumerTmpl, i, i, i)
		if err := os.WriteFile(filepath.Join(dir, "consumer.go"), []byte(content), 0o644); err != nil {
			panic(err)
		}
	}
	fmt.Printf("gen: wrote %d decoupled consumer packages\n", *n)
}
