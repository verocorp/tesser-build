// Command gen emits the two fan-out arms of the decision-3 (no-outward-
// representation) tree as committed generated packages, so CI re-runs the metric
// on inspectable code — see ../../SCORING.md.
//
//   - decoupled/consumerNN     — operate on the domain object's VALUE OBJECTS
//     (domain.Maneuver.Burn()); an outward-representation migration forces 0 edits.
//   - coupled/fanout/consumerNN — reach the DOMAIN-EMITTED DTO's raw wire field
//     (emit.Maneuver.ToResponse().BurnSeconds); forced-edits = N, because the
//     migration removes BurnSeconds from the public response DTO.
//
// The contrast (0 vs N at matched N) is the whole proof for decision 3: the harm
// is that a domain emitting its representation drags N dependents into an outward
// change, whereas the same change through the application service's Respond step
// touches ONE mapping site. Run via
// `go generate ./rationale/changeability/nooutward` (cwd = the nooutward dir).
package main

import (
	"flag"
	"fmt"
	"os"
	"path/filepath"
)

const decoupledTmpl = `// Code generated by internal/gen. DO NOT EDIT.

// Package consumer%02d operates on the domain object's VALUE OBJECTS — never an
// outward representation. The outward-format migration forces 0 edits on it. One
// of N identical dependents; the decoupled arm's forced-edit count stays 0 for
// any N.
package consumer%02d

import "github.com/verocorp/tesser-build/rationale/changeability/nooutward/domain"

// Use reads the domain object through its value objects and nothing outward.
func Use(m domain.Maneuver) int64 {
	return m.Burn().Seconds()
}
`

const coupledTmpl = `// Code generated by internal/gen. DO NOT EDIT.

// Package consumer%02d reaches the DOMAIN-EMITTED DTO (emit.Maneuver.ToResponse())
// and reads a raw wire field. The outward-format migration removes that field, so
// this dependent is forced to change. One of N identical dependents; the coupled
// arm's forced-edit count is N. Fails to build under -tags repv2 because its own
// source names ToResponse().BurnSeconds.
package consumer%02d

import "github.com/verocorp/tesser-build/rationale/changeability/nooutward/emit"

// Use reaches through the domain-emitted outward DTO to a wire-format field.
func Use(m emit.Maneuver) int64 {
	return m.ToResponse().BurnSeconds
}
`

func emit(dir, tmpl string, n int) {
	for i := 0; i < n; i++ {
		pkgDir := filepath.Join(dir, fmt.Sprintf("consumer%02d", i))
		if err := os.MkdirAll(pkgDir, 0o755); err != nil {
			panic(err)
		}
		content := fmt.Sprintf(tmpl, i, i)
		if err := os.WriteFile(filepath.Join(pkgDir, "consumer.go"), []byte(content), 0o644); err != nil {
			panic(err)
		}
	}
}

func main() {
	n := flag.Int("n", 16, "number of consumer packages to emit per arm")
	flag.Parse()

	emit("decoupled", decoupledTmpl, *n)
	emit(filepath.Join("coupled", "fanout"), coupledTmpl, *n)
	fmt.Printf("gen: wrote %d decoupled + %d coupled-fanout consumer packages\n", *n, *n)
}
